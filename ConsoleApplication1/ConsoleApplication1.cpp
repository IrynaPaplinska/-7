#include <iostream>
#include <windows.h>
#include <iomanip>
using namespace std;
// Оголошення констант розміру матриці
const int ROWS = 4;
const int COLS = 4;
int main()
{
    SetConsoleCP(1251);
    SetConsoleOutputCP(1251);
    // Оголошення матриці дійсних чисел 4x4 та змінних
    double A[ROWS][COLS];
    double sum_negative = 0.0;
    int i, j;
    cout << "--- Введення елементів матриці дійсних чисел 4x4 ---" << endl;
    cout << "УВАГА! Введіть від'ємні числа в 1-й та 3-й рядки, щоб сума не була нулем." << endl;
    for (i = 0; i < ROWS; i++)
    {
        for (j = 0; j < COLS; j++)
        {
            cout << "Введіть A[" << i + 1 << "][" << j + 1 << "]: ";
            cin >> A[i][j];
        }
    }
    cout << "\n--- Введена матриця A ---" << endl;
    for (i = 0; i < ROWS; i++)
    {
        for (j = 0; j < COLS; j++)
        {
            cout << fixed << setprecision(2) << setw(8) << A[i][j] << " ";
        }
        cout << endl;
    }
    cout << "\n--- Обчислення суми від'ємних елементів у непарних рядках ---" << endl;
    cout << "Діагностика: Перевіряються лише Рядки 1, 3." << endl;
    for (i = 0; i < ROWS; i++)
    {
        // Умова 1: Перевірка, чи рядок є НЕПАРНИМ (i=0 -> 1-й рядок; i=2 -> 3-й рядок)
        if (i % 2 == 0)
        {
            // Внутрішній цикл: проходимо по всіх стовпцях
            for (j = 0; j < COLS; j++)
            {
                // Умова 2: Перевірка, чи елемент є ВІД'ЄМНИМ
                if (A[i][j] < 0)
                {
                    // ДІАГНОСТИКА: Виводимо елемент перед додаванням до суми
                    cout << "  [ДОДАНО] A[" << i + 1 << "][" << j + 1 << "] = "
                        << fixed << setprecision(2) << A[i][j] << endl;

                    sum_negative += A[i][j];
                }
                // (Примітка: Додатні елементи або нулі в непарних рядках ігноруються мовчки)
            }
        }
        else
        {
            cout << "  [ПРОПУЩЕНО] Весь рядок " << i + 1 << " (Парний рядок)." << endl;
        }
    }
    cout << "\n------------------------------------------------------------" << endl;
    cout << "Загальна сума від'ємних елементів у непарних рядках: "
        << fixed << setprecision(2) << sum_negative << endl;
    cout << "------------------------------------------------------------" << endl;

    return 0;
}